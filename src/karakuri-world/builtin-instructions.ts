    '```',
    '```json',
    '{ "action_id": "sleep-house-a", "duration_minutes": 120, "comment": "2時間ほど休みます。" }',
    '```',
    '```json',
    '{ "target_agent_id": "agent-bob", "items": [{"item_id":"apple","quantity":2}], "money": 50, "comment": "ボブにりんごとお小遣いを渡します。" }',
