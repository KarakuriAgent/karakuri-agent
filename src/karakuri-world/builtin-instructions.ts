    'When you receive a transfer_requested notification:',
    '- STANDALONE offer (you are idle): call `karakuri_world_accept_transfer` or `karakuri_world_reject_transfer` with `transfer_id`. Decide ONLY from the notification text — do NOT call get_world_agents / get_map first, because the offer can time out.',
    '- IN-CONVERSATION offer (you are in_conversation and the offer arrived on a previous turn): on YOUR next speaking turn, ALWAYS pass `transfer_response: "accept"` or `"reject"` to `karakuri_world_conversation_speak` (or `karakuri_world_end_conversation`). Do NOT use `accept_transfer` / `reject_transfer` for in-conversation offers. If you call `conversation_speak` without `transfer_response` while a pending offer is on you, the offer is auto-rejected.',
    '- `transfer` and `transfer_response` are mutually exclusive on conversation_speak.',
    '- `karakuri_world_end_conversation` cannot start a new transfer (only `transfer_response` is allowed).',
    '- While in `in_transfer` state, most execution tools are blocked until the offer is settled; just wait for the next notification.',
    'When a `karakuri_world_conversation_speak` response includes `transfer_status: "failed"` (with a `failure_reason` such as `overflow_inventory_full` / `overflow_money` / `validation_failed` / `persist_failed` / `role_conflict`), the transfer did NOT go through. On your next turn, acknowledge the failure to the other agent (or retry with adjusted items/money if appropriate). `transfer_status: "rejected"` means the receiver declined; `"completed"` means it succeeded; `"pending"` means it is awaiting the receiver.',
    '`karakuri_world_conversation_speak` and `karakuri_world_end_conversation` always require `next_speaker_agent_id`, even in 2-person conversations. Do NOT pass `next_speaker_agent_id` to `karakuri_world_conversation_leave`.',
    'In 2-person conversations, `karakuri_world_end_conversation` ends the whole conversation. In conversations with 3 or more participants, it means you leave while the conversation may continue.',
    '',
    'Typical choices:',
    '- `karakuri_world_get_map`: inspect the whole map when route planning matters (詳細は通知で届く)',
    '- `karakuri_world_get_world_agents`: inspect other agents and their state (詳細は通知で届く)',
    '- `karakuri_world_move`: move to a known destination via `target_node_id`',
    '- `karakuri_world_action`: perform a known action via `action_id`. When the notification shows a range like `30〜480分, duration_minutes: 分数を指定`, also pass `duration_minutes` with a value within that range (結果は通知で届く)',
    '- `karakuri_world_use_item`: use an item from inventory via `item_id` (結果は通知で届く)',
    '- `karakuri_world_transfer`: send items/money to a nearby idle agent via `target_agent_id` (+ `items` and/or `money`, total must be > 0)',
    '- `karakuri_world_accept_transfer`: accept a STANDALONE pending transfer via `transfer_id`',
    '- `karakuri_world_reject_transfer`: reject a STANDALONE pending transfer via `transfer_id`',
    '- `karakuri_world_wait`: wait for change via `duration` (10-minute units, 1-6)',
    '- `karakuri_world_conversation_start`: begin talking with `target_agent_id` and `message`',
    '- `karakuri_world_conversation_accept`: accept and reply with `message`',
    '- `karakuri_world_conversation_reject`: reject an incoming conversation with no arguments',
    '- `karakuri_world_conversation_join`: request to join a nearby ongoing conversation with `conversation_id` (join is applied at the next turn boundary and your first speaking opportunity only comes after you are nominated)',
    '- `karakuri_world_conversation_stay`: answer an `inactive_check` and remain in the conversation (no arguments)',
    '- `karakuri_world_conversation_leave`: answer an `inactive_check` and leave the conversation, optionally with `message`',
    '- `karakuri_world_conversation_speak`: respond during an active conversation with `message` and required `next_speaker_agent_id`; you may also attach `transfer` or `transfer_response` (but not both)',
    '- `karakuri_world_end_conversation`: end or leave a conversation with farewell `message` and required `next_speaker_agent_id`; you may also attach `transfer_response` only. In 2-person conversations this ends the conversation, but in 3+ person conversations it means you leave',
